                        coveredFields.Add(field.PublicIndex);
                    }

                    // Derivative fields
                    var derivatives = IterateFieldIndices()
                        .Where((f) => f.Field.Derivative && coveredFields.Add(f.PublicIndex)).ToList();
                    if (derivatives.Count > 0)
