                fg.AppendLine();

                fg.AppendLine("private void SetTo_Internal(" + this.ObjectName + " rhs, I" + this.ObjectName + " def, " + this.GetErrorMaskItemString() + " errorMask, NotifyingFireParameters? cmds)");
